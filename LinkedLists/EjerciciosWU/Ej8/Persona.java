package LinkedLists.EjerciciosWU.Ej8;

public class Persona {
  private String nombre;
  private int edad;

  public Persona(String nombre) {
    setNombre(nombre);
  }

  public int getEdad() {
    return edad;
  }

  public void setEdad(int edad) {
    this.edad = edad;
  }

  public String getNombre() {
    return nombre;
  }

  public void setNombre(String nombre) {
    this.nombre = nombre;
  }
}
